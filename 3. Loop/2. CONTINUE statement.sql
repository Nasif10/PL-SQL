SET SERVEROUTPUT ON;
SET VERIFY OFF;

--CONTINUE statement

DECLARE
	NUM int:=0; 
BEGIN
	LOOP
		NUM := NUM + 1;
		IF MOD(NUM,2) = 1 THEN
			CONTINUE;
		END IF;
		DBMS_OUTPUT.PUT_LINE(NUM);
		EXIT WHEN NUM = 10;
	END LOOP;	
END;
/


